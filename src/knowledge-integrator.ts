/**
 * Fetches library documentation from Context7 MCP and stores it
 * as a knowledge entry.
 *
 * This function calls the Context7 MCP tools to get the latest
 * documentation about a specific library, then saves it to local storage.
 *
 * @param libraryName - Library/framework name (e.g., "Next.js", "Express", "Prisma")
 * @param topic - Specific topic to search (e.g., "authentication", "middleware")
 * @returns Newly created KnowledgeEntry, or an error entry on failure
 *
 * @example
 * ```ts
 * const entry = await fetchLibraryDocs("Next.js", "middleware");
 * console.log(entry.content);
 * ```
 */
export async function fetchLibraryDocs(
  libraryName: string,
  topic: string,
): Promise<KnowledgeEntry> {
  // Validate input
  if (!libraryName || !libraryName.trim()) {
    throw new Error("Library name must not be empty");
  }
  if (!topic || !topic.trim()) {
    throw new Error("Topic must not be empty");
  }

  const trimmedLibrary = libraryName.trim();
  const trimmedTopic = topic.trim();

  try {
    // Step 1: Resolve library ID from Context7
    const queryText = `How to use ${trimmedTopic} in ${trimmedLibrary}`;

    // Note: Context7 MCP requires a pre-resolved library ID.
    // Since we cannot call MCP tools directly from here
    // (this is library code, not a tool), we create a placeholder entry
    // that can later be updated by the calling agent.

    const entry = createEntry({
      topic: trimmedTopic,
      library: trimmedLibrary,
      source: "context7",
      content:
        `[Context7] Documentation for ${trimmedLibrary} about ${trimmedTopic}.\n` +
        `Query: ${queryText}\n\n` +
        `> This entry is a placeholder for Context7 MCP results.\n` +
        `> The agent should call resolve-library-id then query-docs to fill the content.\n` +
        `> After receiving results, update the entry with appropriate content.`,
      relevance: [trimmedTopic, trimmedLibrary, ...trimmedTopic.split(/\s+/)],
      tags: [trimmedLibrary.toLowerCase(), "context7", "documentation"],
    });

    appendEntry(entry);
    return entry;
  } catch (error) {
    // Create an error entry to keep a record
    const errorEntry = createEntry({
      topic: trimmedTopic,
      library: trimmedLibrary,
      source: "context7",
      content: `Failed to fetch Context7 documentation for ${trimmedLibrary} about ${trimmedTopic}: ${
        error instanceof Error ? error.message : String(error)
      }`,
      tags: [trimmedLibrary.toLowerCase(), "context7", "error"],
    });

    appendEntry(errorEntry);
    return errorEntry;
  }
}

