/**
 * Format a KnowledgeEntry array into human-readable Markdown report.
 *
 * Output format:
 * - Summary of entry count
 * - Each entry displayed with ID, timestamp, source, library, topic
 * - Content truncated if too long
 * - Tags displayed as badges
 *
 * @param entries - Array of KnowledgeEntry to format
 * @returns Formatted Markdown string
 *
 * @example
 * ```ts
 * const report = formatKnowledgeReport(queryResult.entries);
 * console.log(report);
 * // Output:
 * // # Knowledge Report (5 entries)
 * // ...
 * ```
 */
export function formatKnowledgeReport(entries: KnowledgeEntry[]): string {
  if (!entries || entries.length === 0) {
    return "# Knowledge Report\n\n_No entries found._";
  }

  const lines: string[] = [];
  lines.push(`# Knowledge Report`);
  lines.push("");
  lines.push(`**Total entries:** ${entries.length}`);
  lines.push("");

  for (let index = 0; index < entries.length; index++) {
    const entry = entries[index];

    lines.push(`## ${index + 1}. ${entry.library}: ${entry.topic}`);
    lines.push("");
    lines.push(`| Field | Value |`);
    lines.push(`|-------|-------|`);
    lines.push(`| **ID** | \`${entry.id}\` |`);
    lines.push(`| **Timestamp** | ${entry.timestamp} |`);
    lines.push(`| **Library** | ${entry.library} |`);
    lines.push(`| **Source** | ${entry.source} |`);

    if (entry.url) {
      lines.push(`| **URL** | ${entry.url} |`);
    }

    // Tags
    if (entry.tags.length > 0) {
      const tags = entry.tags.map((t) => `\`${t}\``).join(", ");
      lines.push(`| **Tags** | ${tags} |`);
    }

    // Relevance
    if (entry.relevance.length > 0) {
      lines.push(`| **Keywords** | ${entry.relevance.join(", ")} |`);
    }

    lines.push("");

    // Content (truncated for readability)
    const maxContentLength = 500;
    const content =
      entry.content.length > maxContentLength
        ? entry.content.slice(0, maxContentLength) +
          `... _(truncated, ${entry.content.length} chars total)_`
        : entry.content;

    lines.push("### Content");
    lines.push("");
    lines.push(content);
    lines.push("");

    if (index < entries.length - 1) {
      lines.push("---");
      lines.push("");
    }
  }

  return lines.join("\n");
}

/**
 * Format knowledge base statistics into human-readable Markdown.
 *
 * @param stats - KnowledgeStats to format
 * @returns Formatted Markdown string
 *
 * @example
 * ```ts
 * const stats = getKnowledgeStats();
 * console.log(formatKnowledgeStats(stats));
 * ```
 */
export function formatKnowledgeStats(stats: KnowledgeStats): string {
  const lines: string[] = [];

  lines.push("# Knowledge Base Statistics");
  lines.push("");
  lines.push(`**Total entries:** ${stats.totalEntries}`);
  lines.push("");

  // By source
  lines.push("## By Source");
  lines.push("| Source | Count |");
  lines.push("|--------|-------|");
  lines.push(`| Context7 | ${stats.bySource.context7} |`);
  lines.push(`| Web | ${stats.bySource.web} |`);
  lines.push(`| Manual | ${stats.bySource.manual} |`);
  lines.push("");

  // By library (top 15)
  const sortedLibraries = Object.entries(stats.byLibrary)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 15);

  if (sortedLibraries.length > 0) {
    lines.push("## By Library (top 15)");
    lines.push("| Library | Count |");
    lines.push("|---------|-------|");
    for (const [lib, count] of sortedLibraries) {
      lines.push(`| ${lib} | ${count} |`);
    }
    lines.push("");
  }

  // Top topics
  if (stats.topTopics.length > 0) {
    lines.push("## Top Topics");
    lines.push("| Topic | Count |");
    lines.push("|-------|-------|");
    for (const topic of stats.topTopics) {
      lines.push(`| ${topic.topic} | ${topic.count} |`);
    }
    lines.push("");
  }

  return lines.join("\n");
}

/**
 * Format knowledge query results into human-readable Markdown.
 *
 * @param result - KnowledgeQueryResult to format
 * @returns Formatted Markdown string
 *
 * @example
 * ```ts
 * const result = queryProjectKnowledge("Next.js authentication");
 * console.log(formatQueryResult(result));
 * ```
 */
export function formatQueryResult(result: KnowledgeQueryResult): string {
  const lines: string[] = [];

  lines.push(`# Knowledge Query: "${result.question}"`);
  lines.push("");
  lines.push(`**Total matches:** ${result.totalMatches}`);
  lines.push(`**Displayed:** ${result.entries.length}`);
  lines.push("");

  if (result.entries.length === 0) {
    lines.push("_No relevant entries found._");
    lines.push("");
    lines.push("💡 **Tips:**");
    lines.push("- Use more general keywords");
    lines.push("- Try adding knowledge first with `addManualKnowledge()`");
    lines.push("- Or use `fetchLibraryDocs()` to fetch documentation from Context7");
    return lines.join("\n");
  }

  for (let index = 0; index < result.entries.length; index++) {
    const entry = result.entries[index];

    lines.push(`### ${index + 1}. [${entry.source}] ${entry.library} — ${entry.topic}`);
    lines.push("");
    lines.push(`- **ID:** \`${entry.id}\``);
    lines.push(`- **Source:** ${entry.source}`);
    lines.push(`- **Timestamp:** ${entry.timestamp}`);

    if (entry.url) {
      lines.push(`- **URL:** ${entry.url}`);
    }

    if (entry.tags.length > 0) {
      lines.push(`- **Tags:** ${entry.tags.join(", ")}`);
    }

    lines.push("");
    // Content excerpt
    const excerpt =
      entry.content.length > 250 ? entry.content.slice(0, 250) + "..." : entry.content;
    lines.push(excerpt);
    lines.push("");
  }

  return lines.join("\n");
}

/**
 * Format best practice suggestions into human-readable Markdown.
 *
 * @param suggestions - Array of BestPracticeSuggestion
 * @param taskType - Task type (for report title)
 * @param framework - Framework being used
 * @returns Formatted Markdown string
 */
export function formatSuggestions(
  suggestions: BestPracticeSuggestion[],
  taskType: string,
  framework: string,
): string {
  const lines: string[] = [];

  lines.push(`# Best Practices: ${taskType} for ${framework}`);
  lines.push("");
  lines.push(`**Total suggestions:** ${suggestions.length}`);
  lines.push("");

  if (suggestions.length === 0) {
    lines.push("_No best practice suggestions available._");
    lines.push("");
    lines.push("💡 Use `fetchLibraryDocs()` to fetch the latest documentation.");
    return lines.join("\n");
  }

  const priorityLabels: Record<string, string> = {
    high: "🔴 High Priority",
    medium: "🟡 Medium Priority",
    low: "🟢 Nice to Have",
  };

  // Group by priority
  const byPriority: Record<string, BestPracticeSuggestion[]> = { high: [], medium: [], low: [] };
  for (const s of suggestions) {
    const p = s.priority in byPriority ? s.priority : "medium";
    byPriority[p].push(s);
  }

  for (const priority of ["high", "medium", "low"] as const) {
    const group = byPriority[priority] ?? [];
    if (group.length === 0) continue;

    lines.push(`## ${priorityLabels[priority] ?? priority}`);
    lines.push("");

    for (let index = 0; index < group.length; index++) {
      const s = group[index];
      lines.push(`### ${index + 1}. ${s.title}`);
      lines.push("");
      lines.push(s.description);
      lines.push("");
      lines.push(`_Source: ${s.source}_`);
      lines.push("");
    }
  }

  return lines.join("\n");
}
