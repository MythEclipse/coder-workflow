/**
 * Removes a knowledge entry by ID.
 *
 * @param id - ID of the entry to remove
 * @returns true if successfully removed, false if ID was not found
 *
 * @example
 * ```ts
 * const deleted = removeEntry("kn-1712345678901-abc123");
 * if (deleted) {
 *   console.log("Entry successfully deleted");
 * }
 * ```
 */
export function removeEntry(id: string): boolean {
  try {
    const entries = loadEntries();
    const filtered = entries.filter((e) => e.id !== id);

    if (filtered.length === entries.length) {
      return false; // ID not found
    }

    saveAllEntries(filtered);
    return true;
  } catch (error) {
    throw new Error(
      `Failed to remove entry: ${error instanceof Error ? error.message : String(error)}`,
    );
  }
}

/**
 * Gets a knowledge entry by ID.
 *
 * @param id - ID of the entry to find
 * @returns KnowledgeEntry if found, or undefined
 *
 * @example
 * ```ts
 * const entry = getEntryById("kn-1712345678901-abc123");
 * if (entry) {
 *   console.log(`Topic: ${entry.topic}`);
 * }
 * ```
 */
export function getEntryById(id: string): KnowledgeEntry | undefined {
  try {
    const entries = loadEntries();
    return entries.find((e) => e.id === id);
  } catch {
    return undefined;
  }
}

/**
 * Updates an existing entry's content.
 *
 * @param id - ID of the entry to update
 * @param updates - Fields to update
 * @returns Updated entry, or undefined if ID was not found
 *
 * @example
 * ```ts
 * const updated = updateEntry("kn-1712345678901-abc123", {
 *   content: "New and more complete content...",
 *   url: "https://example.com/updated-docs",
 * });
 * ```
 */
export function updateEntry(
  id: string,
  updates: Partial<Pick<KnowledgeEntry, "content" | "url" | "relevance" | "tags" | "topic">>,
): KnowledgeEntry | undefined {
  try {
    const entries = loadEntries();
    const index = entries.findIndex((e) => e.id === id);

    if (index === -1) return undefined;

    // Update the provided fields
    const current = entries[index];

    if (updates.content !== undefined) current.content = updates.content;
    if (updates.url !== undefined) current.url = updates.url;
    if (updates.relevance !== undefined) current.relevance = updates.relevance;
    if (updates.tags !== undefined) current.tags = updates.tags;
    if (updates.topic !== undefined) current.topic = updates.topic;

    // Timestamp is updated to indicate last modification time
    // Original timestamp is preserved in a separate field if needed
    entries[index] = current;

    saveAllEntries(entries);
    return current;
  } catch (error) {
    throw new Error(
      `Failed to update entry: ${error instanceof Error ? error.message : String(error)}`,
    );
  }
}

// ─── Query & Search ─────────────────────────────────────────────────────

/**
 * Calculates the relevance score between a query and an entry.
 * Uses a simple TF-IDF-like approach:
 * - Matches keywords in topic, library, relevance, tags, and content
 * - Each match gives a different weight based on the field
 *
 * @param query - Search query keywords
 * @param entry - Entry whose relevance is being evaluated
 * @returns Relevance score (higher is more relevant)
 */
function calculateRelevance(query: string, entry: KnowledgeEntry): number {
  const qLower = query.toLowerCase();
  const qWords = qLower.split(/\s+/).filter((w) => w.length > 2);

  if (qWords.length === 0) return 0;

  let score = 0;

  // Weight: topic (highest)
  if (entry.topic.toLowerCase().includes(qLower)) {
    score += 10;
  }
  for (const word of qWords) {
    if (entry.topic.toLowerCase().includes(word)) {
      score += 5;
    }
  }

  // Weight: library
  if (entry.library.toLowerCase().includes(qLower)) {
    score += 8;
  }
  for (const word of qWords) {
    if (entry.library.toLowerCase().includes(word)) {
      score += 4;
    }
  }

  // Weight: relevance array (this is specifically designed for matching)
  for (const rel of entry.relevance) {
    if (rel.toLowerCase().includes(qLower)) {
      score += 6;
    } else {
      for (const word of qWords) {
        if (rel.toLowerCase().includes(word)) {
          score += 3;
        }
      }
    }
  }

  // Weight: tags
  for (const tag of entry.tags) {
    if (tag.toLowerCase().includes(qLower)) {
      score += 5;
    } else {
      for (const word of qWords) {
        if (tag.toLowerCase().includes(word)) {
          score += 2;
        }
      }
    }
  }

  // Weight: content (lower, but still important)
  if (entry.content.toLowerCase().includes(qLower)) {
    score += 3;
  }
  for (const word of qWords) {
    // Use regex to count frequency in content
    const escaped = word.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const regex = new RegExp(escaped, "gi");
    const matches = entry.content.match(regex);
    if (matches) {
      score += Math.min(matches.length * 0.5, 5); // cap at 5 per word
    }
  }

  // Bonus: newer entries get a slight boost (decay over 90 days)
  const age = Date.now() - new Date(entry.timestamp).getTime();
  const ageDays = age / 86_400_000;
  if (ageDays < 30) {
    score += 2; // Very recent
  } else if (ageDays < 90) {
    score += 0.5; // Somewhat recent
  }

  return score;
}

/**
 * Searches for knowledge entries relevant to the given question.
 *
 * This function will:
 * 1. Load all entries from storage
 * 2. Calculate relevance score for each entry against the query
 * 3. Sort by score (descending)
 * 4. Return top matches (max 20)
 *
 * @param question - Question or search keywords
 * @returns KnowledgeQueryResult with sorted entries
 *
 * @example
 * ```ts
 * const result = queryProjectKnowledge("how to set up authentication in Next.js?");
 * console.log(`Found ${result.totalMatches} relevant entries`);
 * for (const entry of result.entries) {
 *   console.log(`- [${entry.library}] ${entry.topic}`);
 * }
 * ```
 */
export function queryProjectKnowledge(question: string): KnowledgeQueryResult {
  // Validate input
  if (!question || !question.trim()) {
    return {
      question: question ?? "",
      entries: [],
      totalMatches: 0,
    };
  }

  try {
    const entries = loadEntries();

    if (entries.length === 0) {
      return {
        question: question.trim(),
        entries: [],
        totalMatches: 0,
      };
    }

    // Calculate relevance for each entry
    const scored = entries
      .map((entry) => ({
        entry,
        score: calculateRelevance(question, entry),
      }))
      .filter((item) => item.score >= MIN_RELEVANCE_THRESHOLD);

    // Sort descending by score
    scored.sort((a, b) => b.score - a.score);

    const totalMatches = scored.length;
    const topEntries = scored.slice(0, MAX_QUERY_RESULTS).map((item) => item.entry);

    return {
      question: question.trim(),
      entries: topEntries,
      totalMatches,
    };
  } catch (error) {
    // Errors must not stop the query — return empty result
    console.error(`[knowledge-integrator] Error querying knowledge: ${error}`);
    return {
      question: question.trim(),
      entries: [],
      totalMatches: 0,
    };
  }
}

