/**
 * Generate a contextual tip ("Did you know?") based on task context.
 *
 * This function will:
 * 1. Match the task context against built-in tip keywords
 * 2. Also search in knowledge entries for additional tips
 * 3. Return the most relevant tip for the context
 *
 * @param taskContext - Description of the task context being worked on
 * @returns Formatted tip string, or a default message if no relevant tip is found
 *
 * @example
 * ```ts
 * const tip = generateContextTip("Building authentication with Next.js and Prisma");
 * // Output: "💡 Did you know? Next.js 13+ App Router uses Server Components..."
 * ```
 */
export function generateContextTip(taskContext: string): string {
  if (!taskContext || !taskContext.trim()) {
    return "💡 Tip: Add task context to get relevant suggestions.";
  }

  try {
    const contextLower = taskContext.toLowerCase();
    const matchedTips: Array<{ tip: (typeof BUILT_IN_TIPS)[0]; score: number }> = [];

    // 1. Search from built-in tips
    for (const tip of BUILT_IN_TIPS) {
      let score = 0;

      for (const keyword of tip.keywords) {
        if (contextLower.includes(keyword)) {
          score += 2;
        }

        // Search word by word
        const keywordParts = keyword.split(/\s+/);
        for (const part of keywordParts) {
          if (part.length > 2 && contextLower.includes(part)) {
            score += 1;
          }
        }
      }

      if (score > 0) {
        matchedTips.push({ tip, score });
      }
    }

    // 2. Search from stored knowledge entries
    const knowledgeResult = queryProjectKnowledge(taskContext);
    const knowledgeTips: string[] = [];

    for (const entry of knowledgeResult.entries.slice(0, 3)) {
      // Take the first sentence from content as the tip
      const firstSentence = entry.content.split(/[.!?\n]/).filter(Boolean)[0];
      if (firstSentence && firstSentence.length < 200) {
        knowledgeTips.push(`[${entry.library}] ${firstSentence.trim()}`);
      }
    }

    // 3. Select tip with the highest score
    matchedTips.sort((a, b) => b.score - a.score);

    if (matchedTips.length > 0) {
      const bestTip = matchedTips[0].tip;
      return `💡 Did you know? ${bestTip.message}\n   Why: ${bestTip.reasoning}`;
    }

    if (knowledgeTips.length > 0) {
      return `💡 Did you know? ${knowledgeTips[0]}`;
    }

    return "💡 Tip: Try documenting new knowledge with addManualKnowledge() or fetchLibraryDocs().";
  } catch (error) {
    // Silent fallback — errors should not disrupt UX
    return "💡 Tip: Documentation is a long-term investment. The more knowledge stored, the smarter the suggestions become.";
  }
}

