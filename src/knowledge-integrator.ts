/**
 * Ensures the knowledge integrator storage directory exists.
 * Creates it recursively if it does not exist.
 *
 * @returns Absolute path to the storage directory
 */
function ensureKnowledgeDir(): string {
  const dir = join(process.cwd(), KNOWLEDGE_DIR);
  if (!existsSync(dir)) {
    mkdirSync(dir, { recursive: true });
  }
  return dir;
}

/**
 * Gets the full path to the entries.jsonl file.
 *
 * @returns Absolute path to the entries file
 */
function entriesFilePath(): string {
  return join(ensureKnowledgeDir(), ENTRIES_FILE);
}

/**
 * Reads all entries from the JSONL storage file.
 * Corrupted lines are silently skipped.
 *
 * @returns Array of KnowledgeEntry
 */
function loadEntries(): KnowledgeEntry[] {
  const filePath = entriesFilePath();
  if (!existsSync(filePath)) return [];

  try {
    const content = readFileSync(filePath, "utf-8");
    const entries: KnowledgeEntry[] = [];

    for (const line of content.split("\n").filter(Boolean)) {
      try {
        const parsed = JSON.parse(line) as KnowledgeEntry;

        // Minimal validation: must have id and content
        if (parsed.id && parsed.content !== undefined) {
          entries.push(parsed);
        }
      } catch {
        // skip corrupted lines
      }
    }

    return entries;
  } catch {
    return [];
  }
}

/**
 * Saves an array of entries to the JSONL file.
 * The file is fully overwritten.
 *
 * @param entries - Array of KnowledgeEntry to save
 */
function saveAllEntries(entries: KnowledgeEntry[]): void {
  const filePath = entriesFilePath();
  const tmpPath = filePath + ".tmp";
  const lines = entries.map((e) => JSON.stringify(e));

  try {
    writeFileSync(tmpPath, lines.join("\n") + (lines.length > 0 ? "\n" : ""), "utf-8");
    renameSync(tmpPath, filePath);
  } catch (error) {
    throw new Error(
      `Failed to save knowledge entries: ${error instanceof Error ? error.message : String(error)}`,
    );
  }
}

/**
 * Appends a new entry to the JSONL file.
 *
 * @param entry - KnowledgeEntry to append
 */
function appendEntry(entry: KnowledgeEntry): void {
  try {
    const entries = loadEntries();
    entries.push(entry);
    saveAllEntries(entries);
  } catch (error) {
    throw new Error(
      `Failed to append knowledge entry: ${error instanceof Error ? error.message : String(error)}`,
    );
  }
}

// ─── Entry Generators ───────────────────────────────────────────────────

/**
 * Generates a unique ID for a new entry.
 * Format: kn-{timestamp}-{random6}
 *
 * @returns Unique ID string
 */
function generateEntryId(): string {
  return `kn-${Date.now()}-${Math.random().toString(36).slice(2, 8)}`;
}

/**
 * Factory for creating a new KnowledgeEntry object.
 *
 * @param params - Entry creation parameters
 * @returns KnowledgeEntry ready to save
 */
function createEntry(params: {
  topic: string;
  library: string;
  source: KnowledgeSource;
  content: string;
  url?: string;
  relevance?: string[];
  tags?: string[];
}): KnowledgeEntry {
  return {
    id: generateEntryId(),
    timestamp: new Date().toISOString(),
    topic: params.topic,
    library: params.library,
    source: params.source,
    content: params.content,
    url: params.url,
    relevance: params.relevance ?? [],
    tags: params.tags ?? [],
  };
}

